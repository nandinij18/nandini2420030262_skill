package skill2;

public class TypeConversion {
	 public static void main(String[] args) {
	        int a = 100;
	        double b = a;  
	        double x = 99.99;
	        int y = (int) x;  
	        System.out.println("Implicit conversion (int to double): " + b);
	        System.out.println("Explicit conversion (double to int): " + y);
	    }
	}

